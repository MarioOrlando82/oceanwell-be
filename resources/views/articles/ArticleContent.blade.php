                Climate
                The ocean plays a vital role in climate and weather. The sun's heat causes water to evaporate, adding
                moisture to the air. The oceans provide most of this evaporated water. The water vapor condenses to form
                clouds, which release their moisture as rain or other kinds of precipitation. All life on Earth depends
                on
                this
                process, called the water cycle.
                The atmosphere receives much of its heat from the ocean. As the sun warms the water, the ocean transfers
                heat to the atmosphere. In turn, the atmosphere distributes the heat around the globe.
                Because water absorbs and loses heat more slowly than land masses, the ocean helps balance
                global temperatures by absorbing heat in the summer and releasing it in the winter. Without the ocean to
                help regulate global temperatures, Earth's climate would be bitterly cold.
                Ocean Formation
                After Earth began to form about 4.6 billion years ago, it gradually separated into layers of lighter and
                heavier
                rock. The lighter rock rose and formed Earth's crust. The heavier rock sank and formed Earth's core and
                mantle.
                The ocean's water came from rocks inside the newly forming Earth. As the molten rocks cooled, they
                released
                water vapor and other gases. Eventually, the water vapor condensed and covered the crust with
                a primitive ocean. Today, hot gases from the Earth's interior continue to produce new water at the
                bottom of
                the ocean.
                Ocean Floor
                Scientists began mapping the ocean floor in the 1920s. They used instruments called echo sounders, which
                measure water depths using sound waves. Echo sounders use sonar technology. Sonar is an acronym for
                SOund Navigation And Ranging. The sonar showed that the ocean floor has dramatic physical features,
                including huge mountains, deep canyons, steep cliffs, and wide plains.
                The ocean's crust is a thin layer of volcanic rock called basalt. The ocean floor is divided into
                several
                different
                areas. The first is the continental shelf, the nearly flat, underwater extension of a continent.
                Continental
                shelves
                vary in width. They are usually wide along low-lying land, and narrow along mountainous coasts.
                A shelf is covered in sediment from the nearby continent. Some of the sediment is deposited by rivers
                and
                trapped by features such as natural dams. Most sediment comes from the last glacial period, or Ice Age,
                when
                the oceans receded and exposed the continental shelf. This sediment is called relict sediment.
                At the outer edge of the continental shelf, the land drops off sharply in what is called the continental
                slope. The
                slope descends almost to the bottom of the ocean. Then it tapers off into a gentler slope known as the
                continental rise. The continental rise descends to the deep ocean floor, which is called the abyssal
                plain.
                Abyssal plains are broad, flat areas that lie at depths of about 4,000 to 6,000 meters (13,123 to 19,680
                feet).
                Abyssal plains cover 30 percent of the ocean floor and are the flattest feature on Earth. They are
                covered
                by
                fine-grained sediment like clay and silt. Pelagic sediments, the remains of small ocean organisms, also
                drift
                down from upper layers of the ocean. Scattered across abyssal plains are abyssal hills and underwater
                volcanic peaks called seamounts.
                Rising from the abyssal plains in each major ocean is a huge chain of mostly undersea mountains. Called
                the mid-ocean ridge, the chain circles Earth, stretching more than 64,000 kilometers (40,000 miles).
                Much of
